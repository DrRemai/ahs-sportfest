export function t(key, vars = {}) {
  let str = dict[key] ?? key;
  for (const [k, v] of Object.entries(vars)) {
    str = str.replace(`{${k}}`, v);
  }
  return str;
}

export function tn(keySingular, keyPlural, n, vars = {}) {
  const key = n === 1 ? keySingular : keyPlural;
  return t(key, { ...vars, n });
}

